package com.bristol.domain.catalog;

/**
 * Broad categories for beer styles.
 * These categories are stable and unlikely to change.
 */
public enum BeerStyleCategory {
    ALE,        // IPA, APA, Pale Ale, Blonde, Amber, etc.
    LAGER,      // Pilsner, Vienna, Czech, Munich, etc.
    STOUT,      // Oatmeal Stout, Imperial Stout, Milk Stout, etc.
    WHEAT,      // Hefeweizen, Witbier, etc.
    SOUR,       // Gose, Berliner Weisse, Lambic, etc.
    SPECIALTY   // Fruit beers, Spiced, Experimental, etc.
}
